#!/usr/bin/env ruby

# Assignment: =
base = 12
puts "base = 12 : #{base}"

# Arithmetic: + - * / % // **
puts "base+base : #{base+base}"
puts "base-base : #{base-base}"
puts "base*base : #{base*base}"
puts "base/base : #{base/base}"
puts "base%%base : #{base%base}"
puts "base**base : #{base**base}"

# Comparison: == != >= > <= <
puts "base==12 : #{base==12}"
puts "base!=12 : #{base!=12}"
puts "base>=12 : #{base>=12}"
puts "base>12 : #{base>12}"
puts "base<=12 : #{base<=12}"
puts "base<12 : #{base<12}"

# Logical: and or not
puts "true and false : #{true and false}"
puts "true or false : #{true or false}"
puts "not true : #{not true}"